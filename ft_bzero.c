#include "ft_printf.h"

void	ft_bzero(void *s, size_t n)
{
	int		i;
	char	*arr;

	arr = (char *)s;
	i = 0;
	while (i < (int)n)
	{
		arr[i] = 0;
		i++;
	}
}
